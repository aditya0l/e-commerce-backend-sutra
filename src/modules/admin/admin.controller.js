const prisma = require('../../config/prisma');
const mailer = require('../../utils/mailer');

// ─── Helpers ──────────────────────────────────────────────────────────────────

function orderShape(order) {
    const addr = order.shippingAddress;
    return {
        id: order.id,
        status: order.status.toLowerCase(),
        paymentMethod: order.paymentMethod || 'bank_transfer',
        subtotal: Number(order.subtotal),
        tax: Number(order.tax),
        shipping: Number(order.shipping),
        total: Number(order.totalAmount),
        totalAmount: Number(order.totalAmount),
        currency: order.currency,
        guestEmail: order.guestEmail,
        guestName: order.guestName,
        adminNote: order.adminNote,
        trackingNumber: order.trackingNumber,
        courierName: order.courierName,
        createdAt: order.createdAt,
        shippingAddress: addr ? {
            firstName: addr.firstName, lastName: addr.lastName,
            address: addr.address, city: addr.city,
            state: addr.state, zipCode: addr.zipCode,
            country: addr.country, phone: addr.phone,
        } : null,
        items: (order.items || []).map(i => ({
            id: i.id,
            productId: i.productId,
            name: i.productName,
            sku: i.productSku,
            price: Number(i.unitPrice),
            quantity: i.quantity,
            total: Number(i.totalPrice),
            productSnapshot: i.productSnapshot
        })),
        user: order.user ? { id: order.user.id, name: order.user.name, email: order.user.email } : null,
    };
}

async function uploadImage(req, res) {
    try {
        if (!req.file) {
            return res.status(400).json({ success: false, message: 'No image file provided.' });
        }

        // Return the static path generated by the middleware
        const imageUrl = `/uploads/${req.file.filename}`;

        res.status(200).json({
            success: true,
            data: { url: imageUrl }
        });
    } catch (error) {
        console.error('Image Upload Error:', error);
        res.status(500).json({ success: false, message: 'Image upload failed.' });
    }
}

function productShape(p, categoryData) {
    const stock = p.inventory?.stockQuantity ? p.inventory.stockQuantity - (p.inventory.reservedQuantity || 0) : 0;
    return {
        id: p.id,
        slug: p.slug,
        sku: p.sku,
        name: p.name,
        shortDescription: p.shortDescription,
        description: p.description,
        price: Number(p.price),
        compareAtPrice: p.compareAtPrice ? Number(p.compareAtPrice) : null,
        currency: p.currency,
        images: p.images,
        benefits: p.benefits,
        ingredients: p.ingredients,
        usage: p.usage,
        faq: p.faq,
        certifications: p.certifications,
        tags: p.tags,
        rating: Number(p.rating),
        reviewCount: p.reviewCount,
        isNew: p.isNew,
        isBestseller: p.isBestseller,
        isActive: p.isActive,
        stock,
        category: categoryData || p.category?.name,
        categorySlug: p.category?.slug,
        categoryId: p.categoryId,
    };
}

// ─── Orders ──────────────────────────────────────────────────────────────────

async function listOrders(req, res) {
    const { status, page = 1, limit = 20 } = req.query;
    const where = status ? { status: status.toUpperCase() } : {};
    const [orders, total] = await Promise.all([
        prisma.order.findMany({
            where,
            include: { items: true, shippingAddress: true, user: true },
            orderBy: { createdAt: 'desc' },
            skip: (Number(page) - 1) * Number(limit),
            take: Number(limit),
        }),
        prisma.order.count({ where }),
    ]);
    return res.json({ success: true, data: orders.map(orderShape), meta: { total, page: Number(page), pages: Math.ceil(total / Number(limit)) } });
}

async function updateOrderStatus(req, res) {
    const { id } = req.params;
    const { status, trackingNumber, courierName, adminNote } = req.body;

    const validStatuses = ['pending_payment', 'processing', 'shipped', 'delivered', 'cancelled'];
    if (!validStatuses.includes(status?.toLowerCase())) {
        return res.status(400).json({ success: false, message: `Invalid status. Use: ${validStatuses.join(', ')}` });
    }

    const order = await prisma.order.findUnique({
        where: { id },
        include: { items: true, user: true },
    });
    if (!order) return res.status(404).json({ success: false, message: 'Order not found.' });

    const updateData = { status: status.toUpperCase() };
    if (trackingNumber) updateData.trackingNumber = trackingNumber;
    if (courierName) updateData.courierName = courierName;
    if (adminNote) updateData.adminNote = adminNote;

    const updated = await prisma.order.update({
        where: { id },
        data: updateData,
        include: { items: true, user: true },
    });

    // Fire appropriate email (non-blocking)
    const upperStatus = status.toUpperCase();
    if (upperStatus === 'PROCESSING') {
        mailer.orderPaymentConfirmed(updated).catch(() => { });
    } else if (upperStatus === 'SHIPPED') {
        mailer.orderShipped(updated).catch(() => { });
    } else if (upperStatus === 'DELIVERED') {
        mailer.orderDelivered(updated).catch(() => { });
    } else if (upperStatus === 'CANCELLED') {
        // Release reserved stock
        for (const item of order.items) {
            await prisma.inventory.update({
                where: { productId: item.productId },
                data: { reservedQuantity: { decrement: item.quantity } },
            }).catch(() => { });
        }
        mailer.orderCancelled(updated).catch(() => { });
    }

    return res.json({ success: true, data: orderShape(updated) });
}

// ─── Products ─────────────────────────────────────────────────────────────────

async function listProducts(req, res) {
    const products = await prisma.product.findMany({
        include: { inventory: true, category: true },
        orderBy: { createdAt: 'desc' },
    });
    return res.json({ success: true, data: products.map(p => productShape(p, p.category?.name)) });
}

async function createProduct(req, res) {
    const {
        slug, sku, categoryId, price, compareAtPrice, images, name, shortDescription,
        description, usage, benefits, ingredients, faq, certifications, tags,
        isNew, isBestseller, stock, metaTitle, metaDescription,
    } = req.body;

    const product = await prisma.product.create({
        data: {
            slug, sku, categoryId, price, compareAtPrice: compareAtPrice || null,
            images: images || [], name, shortDescription, description,
            usage: usage || { fr: '', en: '' },
            benefits: benefits || [], ingredients: ingredients || [], faq: faq || [],
            certifications: certifications || [], tags: tags || [],
            isNew: isNew || false, isBestseller: isBestseller || false,
            metaTitle: metaTitle || null, metaDescription: metaDescription || null,
            inventory: { create: { stockQuantity: stock || 0, reservedQuantity: 0 } },
        },
        include: { inventory: true, category: true },
    });
    return res.status(201).json({ success: true, data: productShape(product, product.category?.name) });
}

async function updateProduct(req, res) {
    const { id } = req.params;
    const {
        name, shortDescription, description, price, compareAtPrice,
        images, isNew, isBestseller, isActive, usage, benefits,
        ingredients, faq, certifications, tags, stock,
    } = req.body;

    const updateData = {};
    if (name !== undefined) updateData.name = name;
    if (shortDescription !== undefined) updateData.shortDescription = shortDescription;
    if (description !== undefined) updateData.description = description;
    if (price !== undefined) updateData.price = price;
    if (compareAtPrice !== undefined) updateData.compareAtPrice = compareAtPrice;
    if (images !== undefined) updateData.images = images;
    if (isNew !== undefined) updateData.isNew = isNew;
    if (isBestseller !== undefined) updateData.isBestseller = isBestseller;
    if (isActive !== undefined) updateData.isActive = isActive;
    if (usage !== undefined) updateData.usage = usage;
    if (benefits !== undefined) updateData.benefits = benefits;
    if (ingredients !== undefined) updateData.ingredients = ingredients;
    if (faq !== undefined) updateData.faq = faq;
    if (certifications !== undefined) updateData.certifications = certifications;
    if (tags !== undefined) updateData.tags = tags;

    const product = await prisma.product.update({
        where: { id },
        data: updateData,
        include: { inventory: true, category: true },
    });

    if (stock !== undefined) {
        await prisma.inventory.upsert({
            where: { productId: id },
            create: { productId: id, stockQuantity: stock, reservedQuantity: 0 },
            update: { stockQuantity: stock },
        });
    }

    return res.json({ success: true, data: productShape(product, product.category?.name) });
}

async function deleteProduct(req, res) {
    const { id } = req.params;
    try {
        await prisma.product.update({ where: { id }, data: { isActive: false } });
        return res.json({ success: true, message: 'Product deactivated.' });
    } catch (e) {
        if (e.code === 'P2025') return res.status(404).json({ success: false, message: 'Product not found.' });
        throw e;
    }
}

